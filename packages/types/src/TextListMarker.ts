/**
 * List marker style for `TextFormat.bullet` list items.
 * - `'bullet'`: a filled circle bullet character (default for `format.bullet === true`).
 * - `'none'`: no marker (suppresses bullet even if `format.bullet` is set).
 * `'decimal'`, `'lowerAlpha'`, and `'upperRoman'` are reserved for Silver.
 */
export type TextListMarker = 'bullet' | 'none';
