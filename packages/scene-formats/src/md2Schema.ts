// id Software MD2 (Quake 2 / id Tech 2) wire-format constants. These are format-internal and not
// re-exported from the package barrel.

// Header magic: "IDP2" as a little-endian int32.
export const MD2_MAGIC = 0x32504449;

// Expected version field in the header.
export const MD2_VERSION = 8;

// Fixed header size in bytes: 17 int32 fields = 68 bytes.
export const MD2_HEADER_SIZE = 68;

// Bytes per skin record: a fixed 64-byte null-padded ASCII texture path.
export const MD2_SKIN_SIZE = 64;

// Bytes per triangle record: 3 uint16 vertex indices + 3 uint16 texcoord indices = 12 bytes.
export const MD2_TRIANGLE_SIZE = 12;

// Bytes per texcoord record: int16 s + int16 t = 4 bytes.
export const MD2_TEXCOORD_SIZE = 4;

// Bytes per compressed vertex within a frame: uint8 x + uint8 y + uint8 z + uint8 normalIndex = 4.
export const MD2_COMPRESSED_VERTEX_SIZE = 4;

// Bytes for the per-frame header (scale + translate + name): 3*float32 + 3*float32 + 16 chars = 40.
export const MD2_FRAME_HEADER_SIZE = 40;

